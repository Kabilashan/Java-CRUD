    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int x=JOptionPane.showConfirmDialog(null, "Are you sure you want to delete record?","Delete record",JOptionPane.YES_NO_OPTION);
        if(x==0){
            try{

                String sql="delete from employee_info_1 where Employee_id=?";
                pst=conn.prepareStatement(sql);
                pst.setString(1,txt_empid.getText());
                pst.execute();
                pst.clearParameters();

                JOptionPane.showMessageDialog(null,"Record Deleted");
                clearAll();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        Update_table();
    }//GEN-LAST:event_btn_deleteActionPerformed

