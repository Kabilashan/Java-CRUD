    public void date_time(){
        
        try{
            new Thread(new Runnable() {
                @Override
                public void run() {
                    while (true) {
                        Date d = new Date();
                        String date = d.toString();
                        String arr[] = date.split(" ");
                        String newdate = arr[5] + "-" + arr[1] + "-" + arr[2];
                        Date.setText(newdate);
                        Time.setText(arr[3]);
                    }
                }
            }).start(); 
        }
        catch(Exception e){
            System.out.println(e);
        }
    }
    
