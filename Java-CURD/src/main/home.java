    private void tbl_employee_infoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_employee_infoMouseClicked
        try{
            int row = tbl_employee_info.getSelectedRow();
            String Table_click = (tbl_employee_info.getModel().getValueAt(row,0).toString());
            String sql = "select * from Employee_info_1 where Employee_id='"+Table_click+"' ";
            pst=conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
            
                String add1 = rs.getString("Employee_id");
                txt_empid.setText(add1);
                String add2 = rs.getString("Firstname");
                txt_firstname.setText(add2);
                String add3 = rs.getString("Age");
                txt_age.setText(add3);
            }
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        
        }
    }//GEN-LAST:event_tbl_employee_infoMouseClicked

